from fastapi import FastAPI
from pydantic import BaseModel
from fastapi.middleware.cors import CORSMiddleware
import os
import google.generativeai as genai
from dotenv import load_dotenv

# âœ… Load environment variables
load_dotenv()

# âœ… Initialize FastAPI
app = FastAPI()

# âœ… Enable CORS for frontend
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# âœ… Get Gemini API Key
GEMINI_KEY = os.getenv("GOOGLE_API_KEY")

# âœ… Configure Gemini if key is available
if GEMINI_KEY:
    genai.configure(api_key=GEMINI_KEY)

# âœ… Request Model
class ChatRequest(BaseModel):
    message: str

# âœ… Chat Endpoint
@app.post("/chat")
async def chat_endpoint(request: ChatRequest):
    user_message = request.message.strip()
    print(f"Received: {user_message}")

    # ğŸ”¹ If API key missing â†’ return fallback message
    if not GEMINI_KEY:
        return {"reply": "âš ï¸ Gemini API key not found. Using fallback bot reply."}

    try:
        # âœ… Use the correct model name for Gemini
        model = genai.GenerativeModel("gemini-1.5-flash")  # or "gemini-1.5-pro"
        response = model.generate_content(user_message)

        # âœ… Extract bot reply safely
        bot_reply = getattr(response, "text", None) or "âš ï¸ No reply generated by Gemini."
    except Exception as e:
        bot_reply = f"âŒ Gemini API Error: {str(e)}"

    return {"reply": bot_reply}

# âœ… Root Test Endpoint
@app.get("/")
async def root():
    return {"message": "FastAPI Chatbot with Gemini 1.5 API ğŸš€"}
